print(colorama.Fore.YELLOW + "Sagar Goswami")
print(colorama.Fore.BLUE + "Sagar Goswami")
print(colorama.Fore.MAGENTA + "Sagar Goswami")
print(colorama.Fore.CYAN + "Sagar Goswami\n")

